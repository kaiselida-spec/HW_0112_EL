package com.elidakaiis;

public class HW_0112_EL {
    public static void main(String[] args) {
        System.out.println(2 + 4);
        System.out.println(4 - 2);
        System.out.println(2 * 4);
        System.out.println(4 / 2);

        System.out.println(2.44 + 4);
        System.out.println(2.44 * 4);
        System.out.println(2 - 4.22);
        System.out.println(2 / 4.22);

        System.out.println(2 < 4);
        System.out.println(2 > 4);
        System.out.println(2 >= 4);
        System.out.println(2 <= 4);

        int x = 1000000000;
        int y = 3;
        int result = x * y;
        System.out.println(result);
    }
}

